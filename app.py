def transcribe_audio(audio_path, lang='en'):
    recognizer = sr.Recognizer()
    try:
        with sr.AudioFile(audio_path) as source:
            audio_data = recognizer.record(source)
            text = recognizer.recognize_google(audio_data, language=lang)
            return text
    except sr.UnknownValueError:
        return "Could not understand audio"
    except sr.RequestError as e:
        return f"Error: {str(e)}"
    except Exception as e:
        return f"Processing error: {str(e)}"

def detect_language(text):
    try:
        lang = detect(text)
        return lang if lang in SUPPORTED_LANGUAGES else 'en'
    except:
        return 'en'

def preprocess_text(text, lang='en'):
    try:
        text = text.lower()
        
        if lang == 'ar':
            text = re.sub(r'[^\w\s\u0600-\u06FF]', '', text)
        else:
            text = re.sub(r'[^\w\s]', '', text)
        
        tokens = word_tokenize(text)
        
        if lang == 'ar':
            arabic_stopwords = set(stopwords.words('arabic'))
            tokens = [word for word in tokens if word not in arabic_stopwords]
        elif lang == 'en':
            tokens = [word for word in tokens if word not in stopwords.words('english')]
            
        return " ".join(tokens)
    except Exception:
        return text.lower()

def detect_intent(user_input, lang='en'):
    processed_input = preprocess_text(user_input, lang)
    
    for intent, data in RULES.items():
        if lang in data["patterns"]:
            for pattern in data["patterns"][lang]:
                if lang == 'ar':
                    if pattern in processed_input:
                        return intent
                else:
                    if re.search(r'\b' + re.escape(pattern) + r'\b', processed_input):
                        return intent
    return "default"

def get_response(intent, lang='en'):
    if intent in RULES:
        responses = RULES[intent]["responses"]
        if lang in responses:
            return responses[lang] if isinstance(responses[lang], str) else random.choice(responses[lang])
        return responses['en'] if isinstance(responses['en'], str) else random.choice(responses['en'])
    return random.choice(RULES["default"]["responses"])

